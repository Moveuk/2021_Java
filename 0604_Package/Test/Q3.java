//[문제3] 다음은 숫자 맞추기 계임을 작성한 것이다.
// 1과 100 사이의 값을 반복적으로 입력혜서 컴퓨터가 생각한 값을 맞추면 게임이 끝난다. 
// 사용자가 값을 업력하면, 컴퓨터는 자신이 생각한 값과 비교해서 결과들 알려준다. 
// 사용자가 컴퓨터가 생각한 숫자를 맞추면 게임이 끝나고 몇 번만에 숫자를 맞췄는지 알려준다. 
// ( ) 안에 알맞은 코드를 넣어 프로그램을 완성하시오.
// do {} 부분 작성.

public class Q3 {

	public static void main(String[] args) {
		// 1~100사이의 임의의 값을 얻어서 answer에 저장한다.
		int answer = (int) (Math.random() * 100 + 1);
		int input = 0;
		int count = 0;
		// 화면으로부터 사용자 입력을 밭기 위해서 Scanner 클래스 사용
		java.util.Scanner s = new java.util.Scanner(System.in);
		do {
			count++;
			System.out.print("1과 100사이의 값을 입력하세요 : ");
			input = s.nextInt(); // 입력 받은 값을 변수 input에 저장한다.
			
			if (answer > input) {
				System.out.println("더 큰 수를 입력하세요.");
			} else if (answer < input) {
				System.out.println("더 작은 수를 입력하세요.");
			} else {
				System.out.println("맞췄습니다. 시도횟수는 "+count+"번입니다.");
				break;
			}
			
		}while(true);

	}

}


// do ~ while 문 활용, if 문 활용, count, scanner